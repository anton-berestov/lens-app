<template>
  <ion-page>
    <Header back title="Линзы" />
    <ion-content>
      <ion-list class="container">
        <ion-row class="ion-margin wrapper" style="position: relative">
          <div class="sale" v-if="product.sale">
            <p class="sale-text">{{ `-${product.sale}%` }}</p>
          </div>
          <swiper
            ref="swiper"
            :options="slideOpts"
            :modules="modules"
            :pagination="true"
            :autoplay="true"
            class="swiper"
          >
            <swiper-slide v-for="(image, index) in product.image" :key="index">
              <img :src="image" class="image" />
            </swiper-slide>
          </swiper>
        </ion-row>
        <ion-row class="ion-margin">
          <ion-row class="row">
            <ion-label class="title">{{ product.name }}</ion-label>
          </ion-row>
          <ion-row class="row">
            <ion-label class="title">{{ product.producer }}</ion-label>
          </ion-row>
          <ion-row class="ion-margin-top row">
            <ion-label class="price">{{ product.price }}</ion-label>
            <ion-label
              v-if="product.old_price"
              class="ion-margin-start old-price"
              >{{ product.old_price }}
            </ion-label>
          </ion-row>
        </ion-row>
        <ion-row class="ion-justify-content-between">
          <ion-label
            :class="['text', { active: specification }, 'ion-margin']"
            @click="
              () => {
                specification = true;
                description = false;
                delivery = false;
              }
            "
            >Характеристики
          </ion-label>
          <ion-label
            :class="['text', { active: description }, 'ion-margin']"
            @click="
              () => {
                specification = false;
                description = true;
                delivery = false;
              }
            "
            >Описание
          </ion-label>
          <ion-label
            :class="['text', { active: delivery }, 'ion-margin']"
            @click="
              () => {
                specification = false;
                description = false;
                delivery = true;
              }
            "
            >Доставка
          </ion-label>
        </ion-row>
        <ion-row>
          <Specification
            v-if="specification"
            :brand="brand.name"
            :producer="producer.name"
            :type="type.name"
            :period="period.name"
            :radius="radius.name"
            :diameter="diameter.name"
            :material="material.name"
          />
          <Description v-if="description" />
          <Delivery v-if="delivery" />
        </ion-row>

        <ion-row class="ion-justify-content-center">
          <Button
            title="В корзину"
            class="custom-btn"
            @click="addInCart(product)"
          />
        </ion-row>
      </ion-list>
    </ion-content>
  </ion-page>
</template>

<script lang="js">
import {defineComponent} from 'vue';
import {IonPage, IonContent, IonRow, IonList, IonLabel} from '@ionic/vue';
import {Swiper, SwiperSlide} from 'swiper/vue';
import {
  Controller,
  Pagination,
  Autoplay,
  Keyboard,
  Scrollbar,
  Zoom,
} from 'swiper';
import {IonicSlides} from '@ionic/vue';
import Header from '@/components/ui/Header.vue';
import Specification from '@/components/Specification.vue';
import Description from '@/components/Description.vue';
import Delivery from '@/components/Delivery.vue';
import Button from "@/components/ui/Button.vue";
import {mapMutations} from "vuex";

import products from '../../public/mocha/products/products.json';
import radius from '../../public/mocha/radius.json';
import periods from '../../public/mocha/periods.json';
import types from '../../public/mocha/types.json';
import sphere from '../../public/mocha/sphere.json';
import producer from '../../public/mocha/producer.json';
import brand from '../../public/mocha/brand.json';
import material from '../../public/mocha/material.json';

import 'swiper/css';
import 'swiper/css/autoplay';
import 'swiper/css/keyboard';
import 'swiper/css/pagination';
import 'swiper/css/scrollbar';
import 'swiper/css/zoom';
import '@ionic/vue/css/ionic-swiper.css';


export default defineComponent({
  name: 'Product',
  props: {
    id: {
      type: String || Number,
      default: null,
    },
  },
  components: {
    Button,
    Header,
    IonPage,
    IonContent,
    IonRow,
    IonList,
    Swiper,
    SwiperSlide,
    IonLabel,
    Specification,
    Description,
    Delivery
  },
  data() {
    return {
      slideOpts: {
        initialSlide: 1,
        speed: 400,
      },
      modules: [
        Pagination,
        Controller,
        Autoplay,
        Keyboard,
        Scrollbar,
        Zoom,
        IonicSlides,
      ],
      specification: true,
      description: false,
      delivery: false,
    };
  },
  computed: {
    product() {
      return products.find((el) => el.id == this.idProduct);
    },
    idProduct() {
      return this.id;
    },
    radius () {
      return radius.find((el)=> el.id === this.product.radius_id) || ''
    },
    period() {
      return periods.find((el)=> el.id === this.product.period_id) || ''
    },
    type(){
      return types.find((el)=> el.id === this.product.type_id) || ''
    },
    diameter(){
      return sphere.find((el)=> el.id === this.product.sphere_id) || ''
    },
    brand(){
      return brand.find((el)=> el.id === this.product.brand_id) || ''
    },
    producer(){
      return producer.find((el)=> el.id === this.product.producer_id) || ''
    },
    material(){
      return material.find((el)=> el.id === this.product.material_id) || ''
    }
  },
  methods: {
    ...mapMutations(['SET_CART']),
    addInCart(product) {
      this.SET_CART(product)
    }
  },
});
</script>

